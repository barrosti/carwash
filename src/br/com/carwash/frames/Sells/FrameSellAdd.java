    private void loadInitialData() {
        try {
            Vector<User> users = new Vector<User>(UserDao.getInstance().getAllUsers());
            DefaultComboBoxModel dcbm = new DefaultComboBoxModel(users);
            comboVendors.setModel(dcbm);
            fieldTotal.setText("R$ 0,00");
            fieldDate.setText(new SimpleDateFormat("dd/mm/yyyy").format(new Date()));
        } catch (SQLException ex) {
            Logger.getLogger(FrameSellAdd.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Erro loading default data", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }

    public void refreshItens() {
        float value = 0;
        for (SellItem sellItem : itens) {
            value += sellItem.getProduct().getPrice() * sellItem.getQnt();
            sellItem.setTotalOfItem(sellItem.getProduct().getPrice() * sellItem.getQnt());
        }
        tabelItens.setModel(new JTableControl(SellItem.class, itens, tabelItens));
        fieldTotal.setText("R$ " + value);
    }
