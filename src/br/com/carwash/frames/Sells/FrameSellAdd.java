    private void btnRemoveItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveItemActionPerformed

        if (tabelItens.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "You must select one before remove", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int opt = JOptionPane.showConfirmDialog(this, "Are you sure ?", "Confirm Delete", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (opt == JOptionPane.YES_OPTION) {
            itens.remove(tabelItens.getSelectedRow());
            refreshItens();
        }
    }//GEN-LAST:event_btnRemoveItemActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        if (Util.isEmpty(itens)) {
            JOptionPane.showMessageDialog(this, "The item list is empty !", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int opt = JOptionPane.showConfirmDialog(this, "Are you sure ?", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (opt == JOptionPane.YES_OPTION) {

            Sell sell = new Sell();
            sell.setDataOfSale(new java.sql.Date(new java.util.Date().getTime()));
            sell.setTotal(Float.parseFloat(fieldTotal.getText().substring(2)));
            sell.setItens(itens);
            for (SellItem sellItem : itens) {
                sellItem.setSell(sell);
            }

            sell.setUser((User) comboVendors.getSelectedItem());
            try {
                SellDao.getInstance().addSell(sell, true);
                control.loadInitialData();
                setVisible(false);
            } catch (SQLException ex) {
                Logger.getLogger(FrameSellAdd.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Error saving sell " + ex, "Error", JOptionPane.ERROR_MESSAGE);

            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed
