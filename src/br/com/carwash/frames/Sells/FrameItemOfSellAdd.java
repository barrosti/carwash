    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        Integer qnt = Integer.parseInt(fieldQnt.getText());

        if (qnt <= 0) {
            JOptionPane.showMessageDialog(this, "Qnt must be higher than 0","Error",JOptionPane.ERROR_MESSAGE);
            fieldQnt.requestFocus();
            return;
        }

        int opt = JOptionPane.showConfirmDialog(this,"Are you sure ?","Add new Item",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);

        if (opt == JOptionPane.YES_OPTION) {
           SellItem item = new SellItem();
           item.setQnt(qnt);
           item.setProduct((Product) comboProduct.getSelectedItem());
           itens.add(item);
           if (sellForm != null) {
               sellForm.refreshItens();
           } else {
               updateForm.refreshItens();
           }
           setVisible(false);
        }
    }//GEN-LAST:event_btnSaveActionPerformed


