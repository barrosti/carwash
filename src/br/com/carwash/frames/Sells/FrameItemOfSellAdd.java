    private void loadComboData() {
        try {
            Vector<Product> produtcts = new Vector<Product>(ProductDao.getInstance().getAllProducts());
            comboProduct.setModel(new DefaultComboBoxModel(produtcts));
        } catch (SQLException ex) {
            Logger.getLogger(FrameItemOfSellAdd.class.getName()).log(Level.SEVERE, null, ex);
        }


    }

