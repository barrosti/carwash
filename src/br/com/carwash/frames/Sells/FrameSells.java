    public void loadInitialData() {
        try {
            sells = SellDao.getInstance().getAllSells();
            tableSells.setModel(new JTableControl(Sell.class, sells, tableSells));
            tableItens.setModel(new JTableControl(SellItem.class,new LinkedList<SellItem>() , tableItens));
        } catch (SQLException ex) {
            Logger.getLogger(FrameSells.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Cannot load the sells","Error",JOptionPane.ERROR_MESSAGE);
        }

    }

    private void addSellTableListener() {
        tableSells.getSelectionModel().addListSelectionListener(new ListSelectionListener() {

            public void valueChanged(ListSelectionEvent e) {
                try{
                Sell selectedSell = sells.get(tableSells.getSelectedRow());
                tableItens.setModel(new JTableControl(SellItem.class,selectedSell.getItens() , tableItens));
                  } catch (Exception ex) {

                }
            }
        });
    }

