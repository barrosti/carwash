    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if (tableSells.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this,"You must select one sell first","Error",JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        Sell selectedSell = sells.get(tableSells.getSelectedRow());

        FrameSellUpdate dialog = new FrameSellUpdate(new Frame(),true, this,selectedSell);
        dialog.setVisible(true);
        // TODO add your handling code here:
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if (tableSells.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this,"You must select one sell firt !","Error",JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        int result = JOptionPane.showConfirmDialog(this,"Are you sure ?","Delete a Sell",JOptionPane.YES_NO_OPTION);
        if (result == JOptionPane.YES_OPTION) {
            try {
                SellDao.getInstance().removeSell(sells.get(tableSells.getSelectedRow()).getId());
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this,"Error Removing Sell","Error",JOptionPane.ERROR_MESSAGE);
            }
           loadInitialData();
        }
    }//GEN-LAST:event_btnRemoveActionPerformed


