    private void loadInitialComboData() {
        try {
            comboType.setModel(new DefaultComboBoxModel(ScheduleType.values()));
            List<Car> animals = CarDao.getInstance().getAllCars();
            DefaultComboBoxModel cmb = new DefaultComboBoxModel(new Vector<Car>(animals));
            comboCar.setModel(cmb);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading data");
            return;
        }
    }

    private void loadScheduleDataInComobBoxes() {
        comboCar.setSelectedItem(schedule.getCar());
        comboType.setSelectedItem(schedule.getType());
        fieldTotal.setText(String.valueOf(schedule.getTotal()));
    }
