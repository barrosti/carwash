    private String verifyMandatoryFields() {
        String messageError = "";
        if (Util.isEmpty(fieldTotal.getText())) {
            messageError = String.format("The field %s is Mandatory!", "Total");
        } 
        return messageError;
    }      
    
    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

       String error = verifyMandatoryFields();
       
       if (!Util.isEmpty(error)) {
            JOptionPane.showMessageDialog(this, error, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }        

        int resp = JOptionPane.showConfirmDialog(this, "Are you sure ?","Update the schedule",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if (resp == JOptionPane.NO_OPTION) {
            return;
        }

        try {
        schedule.setCar((Car) comboCar.getSelectedItem());
        schedule.setType((ScheduleType) comboType.getSelectedItem());
        schedule.setDate(new java.sql.Date(Calendar.getInstance().getTimeInMillis()));
        schedule.setTotal(Float.parseFloat(fieldTotal.getText()));
        ScheduleDao.getInstance().updateSchedule(schedule);
        frame.loadInitialData();
        setVisible(false);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error updating schedule");
            return;
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

