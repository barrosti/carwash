    private String verifyMandatoryFields() {
        String messageError = "";
        if (Util.isEmpty(fieldTotal.getText())) {
            messageError = String.format("The field %s is Mandatory!", "Total");
        } 
        return messageError;
    }    
    
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

       String error = verifyMandatoryFields();
       
       if (!Util.isEmpty(error)) {
            JOptionPane.showMessageDialog(this, error, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }        

        int resp = JOptionPane.showConfirmDialog(this, "Are you sure ?", "Add new schedule", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (resp == JOptionPane.NO_OPTION) {
            return;
        }

        try {
            Schedule sch = new Schedule();
            sch.setCar((Car) comboCar.getSelectedItem());
            sch.setType((ScheduleType) comboType.getSelectedItem());
            sch.setDate(new java.sql.Date(Calendar.getInstance().getTimeInMillis()));
            sch.setTotal(Float.parseFloat(fieldTotal.getText()));
            ScheduleDao.getInstance().addSchedule(sch);
            frame.loadInitialData();
            setVisible(false);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error adding new schedule");
            return;
        }
    }//GEN-LAST:event_btnSaveActionPerformed
