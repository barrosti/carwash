    private void loadInitialComboData() {
        try {
            comboType.setModel(new DefaultComboBoxModel(ScheduleType.values()));
            List<Car> cars = CarDao.getInstance().getAllCars();
            DefaultComboBoxModel cmb = new DefaultComboBoxModel(new Vector<Car>(cars));
            comboCar.setModel(cmb);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading data");
            return;
        }
    }
