    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        String error = verifyMandatoryFields();
        
        if(!Util.isEmpty(error)){
            JOptionPane.showMessageDialog(this, error, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }         

        int result = JOptionPane.showConfirmDialog(this, "Are you sure ?", "Add new Product", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (result == JOptionPane.NO_OPTION) {
            return;
        }

        try {
            Product prod = new Product();
            prod.setName(fieldName.getText());
            prod.setPrice(Float.parseFloat(fieldPrice.getText()));
            if(!Util.isEmpty(fieldStock.getText())){
                prod.setStock(Integer.parseInt(fieldStock.getText()));                
            }
            ProductDao.getInstance().addProduct(prod);
            control.loadInitialData();
            setVisible(false);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error adding product");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private String verifyMandatoryFields(){
        String messageError = "";
        if(Util.isEmpty(fieldName.getText())){
            messageError = String.format("The field %s is Mandatory!", "Name");
        } else if(Util.isEmpty(fieldPrice.getText())){
            messageError = String.format("The field %s is Mandatory!", "Price");
        }
        return messageError;
    }        
    
