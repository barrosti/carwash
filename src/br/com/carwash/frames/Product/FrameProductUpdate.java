    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        if (fieldName.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter name");
            fieldName.requestFocus();
            return;
        }
        if (fieldPrice.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter price");
            fieldPrice.requestFocus();
            return;
        }

        int result = JOptionPane.showConfirmDialog(this, "Are you sure ?", "Updating Product", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (result == JOptionPane.NO_OPTION) {
            return;
        }

        try {
            productEditable.setName(fieldName.getText());
            productEditable.setPrice(Float.parseFloat(fieldPrice.getText()));
            productEditable.setStock(Integer.parseInt(fieldStock.getText()));
            ProductDao.getInstance().updateProduct(productEditable);
            control.loadInitialData();
            setVisible(false);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error updating product");
        }


    }//GEN-LAST:event_btnUpdateActionPerformed

