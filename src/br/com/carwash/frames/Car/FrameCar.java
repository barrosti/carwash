    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        try {
            if (tableCars.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(this, "Please select a car!");
                return;
            }
            int resp = JOptionPane.showConfirmDialog(this, "Are you sure ?", "Delete a Car", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (resp == JOptionPane.CANCEL_OPTION) {
                return;
            }
            CarDao.getInstance().removeCar(cars.get(tableCars.getSelectedRow()).getId());
            loadInitialData();

        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error removing animal");
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        if (tableCars.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Please select a car !");
            return;
        }
        FrameCarUpdate dialog = new FrameCarUpdate(new javax.swing.JFrame(), true, this, cars.get(tableCars.getSelectedRow()));
        dialog.setVisible(true);
    }//GEN-LAST:event_btnUpdateActionPerformed

