    public void loadInitialData() {
        try {
            cars = CarDao.getInstance().getAllCars();
            tableCars.setModel(new JTableControl(Car.class, cars, tableCars));
        } catch (SQLException ex) {
            Logger.getLogger(FrameCar.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
