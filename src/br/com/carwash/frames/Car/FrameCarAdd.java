    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

       String error = verifyMandatoryFields();
       
       if (!Util.isEmpty(error)) {
            JOptionPane.showMessageDialog(this, error, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int resp = JOptionPane.showConfirmDialog(this, "Are you sure ?", "Add new Car", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (resp == JOptionPane.CANCEL_OPTION) {
            return;
        }

        try {
            Car car = new Car();
            car.setMark(fieldMark.getText());
            car.setModel(fieldModel.getText());
            car.setType((CarType) comboType.getSelectedItem());
            car.setClient((Client) comboClient.getSelectedItem());
            car.setYear(Integer.parseInt(fieldYear.getText()));
            car.setPlaque(fieldPlaque.getText());
            CarDao.getInstance().addCar(car);
            control.loadInitialData();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error adding new car, try again !");
            return;
        }
        setVisible(false);
    }//GEN-LAST:event_btnSaveActionPerformed

