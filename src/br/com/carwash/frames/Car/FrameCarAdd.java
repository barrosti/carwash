    private void loadInitialComboData() {
        // The Type Combo
        DefaultComboBoxModel cbm = new DefaultComboBoxModel(CarType.values());
        comboType.setModel(cbm);

        // The Client Combo
        try {
            Vector<Client> clients = new Vector<Client>(ClientDao.getInstance().getAllClients());
            DefaultComboBoxModel comboClients = new DefaultComboBoxModel(clients);
            comboClient.setModel(comboClients);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading the clients list");
        }
    }
